from enum import IntEnum, unique

@unique
class config(IntEnum):

      DEFAULT: bin = 0b00000000000000000000
      SAVE_WEIGHTS: bin = 0b00000000000000000001
      LOAD_WEIGHTS: bin = 0b00000000000000000010
      LOSS_EVENT: bin = 0b00000000000000000100
      HAMMING_LOSS_EVENT: bin = 0b00000000000000001000
      MACRO_PRECISION_EVENT: bin = 0b00000000000000010000
      MACRO_RECALL_EVENT: bin = 0b00000000000000100000
      MACRO_FI_SCORE_EVENT: bin = 0b00000000000001000000
      MICRO_PRECISION_EVENT: bin = 0b00000000000010000000
      MICRO_RECALL_EVENT: bin = 0b00000000000100000000
      MICRO_FI_SCORE_EVENT: bin = 0b00000000001000000000
      MACRO_TP_EVENT: bin = 0b00000000010000000000
      MACRO_FP_EVENT: bin = 0b00000000100000000000
      MACRO_TN_EVENT: bin = 0b00000001000000000000
      MACRO_FN_EVENT: bin = 0b00000010000000000000
      MICRO_TP_EVENT: bin = 0b00000100000000000000
      MICRO_FP_EVENT: bin = 0b00001000000000000000
      MICRO_TN_EVENT: bin = 0b00010000000000000000
      MICRO_FN_EVENT: bin = 0b00100000000000000000

      @staticmethod
      def show() -> None:
          print("SAVE_WEIGHTS")
          print("LOAD_WEIGHTS")
          print("LOSS_EVENT")
          print("HAMMING_LOSS_EVENT")
          print("MACRO_PRECISION_EVENT")
          print("MACRO_RECALL_EVENT")
          print("MACRO_FI_SCORE_EVENT")
          print("MICRO_PRECISION_EVENT")
          print("MICRO_RECALL_EVENT")
          print("MICRO_FI_SCORE_EVENT")
          print("MACRO_TP_EVENT")
          print("MACRO_FP_EVENT")
          print("MACRO_TN_EVENT")
          print("MICRO_FN_EVENT")
          print("MICRO_TP_EVENT")
          print("MICRO_FP_EVENT")
          print("MICRO_TN_EVENT")
